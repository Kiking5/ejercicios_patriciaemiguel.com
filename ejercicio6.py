"""Escribí un programa que solicite al usuario ingresar tres números para 
luego mostrarle el promedio de los tres."""

num1 = float(input("Ingrese un número: "))
num2 = float(input("Ingrese un segundo número: "))
num3 = float(input("Ingrese un tercer número: "))
promedio = (num1 + num2 + num3)/3
print(f"El promedio de los tres números es de {promedio}")